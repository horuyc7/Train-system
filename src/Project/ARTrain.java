        String password = "admin";
        String url = "jdbc:mysql://localhost:3306/project2";

        String query = "INSERT INTO train values (?, ?, ?)";

        Connection conn =null;
        PreparedStatement st = null;
        ResultSet rs = null;

        String userinput;
        String userinput1;
        String userinput2;

        int a,b,c;

        userinput = jTextField1.getText();
        userinput1 = jTextField2.getText();
        userinput2 = jTextField3.getText();

