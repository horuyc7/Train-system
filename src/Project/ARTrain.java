        String user = "root";
        String password = "admin";
        String url = "jdbc:mysql://localhost:3306/project2";
        
        String query = "UPDATE train SET Max_Passenger_Capacity = ?, Crusing_Speed= ? WHERE Train_ID = ?";
        
        Connection conn=null;
        PreparedStatement st=null;
        ResultSet rs=null;
        

        String userinput;
        String userinput1;
        String userinput2;
        
        int e,f,g;
        
