    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String user = "root";
        String password = "admin";
        String url = "jdbc:mysql://localhost:3306/project2";
        
        String query = "SELECT * FROM route";
        
        Connection c=null;
        PreparedStatement st=null;
        ResultSet rs=null;
        
        
        
        try {
            
            c = DriverManager.getConnection(url, user, password);
            
            if(c!=null)
            {
                System.out.print("Connect\n");
            }
            else
            {
                 System.out.print("Fail\n");
            }
            
            jTable2.setVisible(false);
            jTable1.setVisible(true);
            
            st = c.prepareStatement(query);
            
            rs = st.executeQuery(query);
            
            while(rs.next())
            {
                String b = String.valueOf(rs.getInt("Route_No"));
                String d = String.valueOf(rs.getInt("Starting_Station_No"));
                String e = String.valueOf(rs.getInt("Ending_Station_No"));
                String f = String.valueOf(rs.getInt("Length"));
                
                String tbData[] = {b, d, e ,f};
                DefaultTableModel tblModel = (DefaultTableModel)jTable1.getModel();
                
                tblModel.addRow(tbData);
            }
            
            
        } catch (SQLException ex) {
            Logger.getLogger(View.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

